// else if Statement

// Used when checking multiple conditions.
// JavaScript checks conditions one by one.

// Syntax
if (condition1) {

} else if (condition2) {

} else {

}

// Example
var A = 10;
var B = 10;

if (A > B) {
   console.log("A is greater");
}
else if (A === B) {
   console.log("A and B are equal");
}
else {
   console.log("B is greater");
}