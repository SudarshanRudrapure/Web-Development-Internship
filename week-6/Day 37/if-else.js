// if...else Statement
// Used when there are two possible results.

// If condition is true → first block runs
// If condition is false → else block runs

// Syntax

if (condition) {
   // true block
} else {
   // false block
}

// Example
var A = 5;
var B = 10;

if (A > B) {
   console.log("A is greater");
} else {
   console.log("B is greater");
}