// Declaring a variable
var x;
x = 5;
alert(x);

// Declaring and assigning value
var word = "John";
alert(word);

// Number variable
var age = 21;
alert(age);

// Using variable in expression
var a = 10;
var b = 5;
var sum = a + b;
alert(sum);

// Subtraction using variables
var x = 20;
var y = 8;
var result = x - y;
alert(result);

// Multiplication
var a = 4;
var b = 3;
var result = a * b;
alert(result);

// Division
var a = 20;
var b = 4;
var result = a / b;
alert(result);

// Changing variable value
var score = 50;
score = 80;
alert(score);

// String variable
var city = "Bangalore";
alert(city);

// Boolean variable
var isStudent = true;
alert(isStudent);